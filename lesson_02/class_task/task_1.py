# Task 1 - Basic Arithmetic
a = 12
b = 4
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")

# Task 2 - Complex Expression
x = 3
y = 2
result = x - (y / (1 + x * y))
print(f"Result = {result}")

# Task 3 - Averages
num1 = 16
num2 = 9
regular_avg = (num1 + num2) / 2
geometric_avg = (num1 * num2) ** 0.5
print(f"Regular Average = {regular_avg}")
print(f"Geometric Average = {geometric_avg}")

# Task 4 - Triangle
side1 = 6
side2 = 8
hypotenuse = (side1**2 + side2**2) ** 0.5
area = (side1 * side2) / 2
print(f"Hypotenuse = {hypotenuse}")
print(f"Area = {area}")

